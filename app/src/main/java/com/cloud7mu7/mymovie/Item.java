package com.cloud7mu7.mymovie;

public class Item {

    int no;
    String title;
    String day;
    String moviename;
    String coment;

    public Item(){
    }

    public Item(int no, String title, String moviename, String coment){
        this.no = no;
        this.title = title;
        this.day = day;
        this.moviename = moviename;
        this.coment = coment;
    }
}
